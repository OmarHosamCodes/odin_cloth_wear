/// A class that contains constant route names used in the application.
class Routes {
  /// The root route for the home screen.
  static const String homeRoot = 'home';

  /// The route for the home screen.
  static const String home = '/';

  /// The root route for the item screen.
  static const String itemRoot = 'item';

  /// The route for the item screen with a dynamic parameter 'id'.
  static const String item = '/item/:id';

  /// The root route for the cart screen.
  static const String cartRoot = 'cart';

  /// The route for the cart screen.
  static const String cart = '/cart';

  /// The route for the page not found screen.
  static const String pageNotFound = '/404';
}
